/***************************************************************************************************************
 *                                               VOFA数据发送相关宏定义
 **************************************************************************************************************/

/* 
 * 当前使用的通信协议：
 * FireWater:
 *     该协议为CSV格式的字符串协议，可直接用printf打印。
 *     发送字符串即可，上位机以逗号分割，自动分配通道。
 *     适用于对传输速度要求不高的场景，如参数调试、波形显示等。
 *     FireWater协议不限制发送数据的频率，方便用户自由控制发送节奏。
 * RawData:
 *     RawData协议适用于不需要格式化数据，仅查看字节流的场景。
 *     该协议只有VOFA+能够解析，不支持任何第三方数据和图片显示，仅用于调试。
 * JustFloat:
 *     该协议为小端浮点格式化的字节流协议，与十进制转换无关，节省计算时间。
 *     该协议非常节省带宽，适用于对传输速度要求较高的场景。
 *     字节流数据必须选择十六进制显示，十进制格式的打印字符串只能显示乱码。
 */
#define VOFA_DATA_FORMAT "FireWater"

/*
 * 打印函数定义，根据实际平台修改
 */
#define VOFA_printf     printf

/*
 * 数据类型标志，用于选择不同的数据显示模式
 * 对应vofa_draw_graphical函数中的switch case分支
 */
#define PID_LEFT_MOTOR  0   /* 左电机速度环PID参数 */
#define PID_RIGHT_MOTOR 1   /* 右电机速度环PID参数 */
#define PID_TURN        2   /* 转向环PID参数 */
#define PID_ANGLE       3   /* 角度环PID参数 */

/***************************************************************************************************************
 *                                               VOFA数据接收相关宏定义
 **************************************************************************************************************/

/*
 * VOFA+ 数据包格式定义：
 *                                                                 head_pos位                               end_pos位
 * +---------------------------------------------------------------+--------+---------------+----------------+
 * | 数据标识符(用于区分下发的是什么数据,1-2byte) | 帧头('=',1byte) | 数据位(n byte) | 帧尾('\n',1byte) |
 * +---------------------------------------------------------------+--------+---------------+----------------+
 * 
 * 例如:
 *     "P1=%.2f\n"  // 设置角度环P参数
 *     "I2=%.2f\n"  // 设置左电机速度环I参数
 * 
 * 说明：数据标识符用于区分不同的参数，如P1表示角度环P参数，I2表示左电机速度环I参数等
 */
#define VOFA_DATAPACK_HEAD      '='     /* VOFA数据包帧头 */
#define VOFA_DATAPACK_END       '\n'    /* VOFA数据包帧尾 */
#define VOFA_DATAPACK_MAXLEN    200     /* VOFA数据包最大长度 */

/*******************************************************************************
 * @brief   向VOFA+上位机发送数据，用于图形化显示
 * @param   _dataflag 当前发送的数据类型标志位，用于选择不同的数据显示模式
 * @return  无
 * @note    使用FireWater协议格式："<prefix>:ch0,ch1,ch2,...,chN\n"
 *          支持多种数据类型的发送，通过_dataflag参数选择不同的数据显示模式
 *******************************************************************************/
void vofa_draw_graphical(uint8_t _dataflag);

/*******************************************************************************
 * @brief   通过串口接收来自VOFA+的数据包，并根据数据包执行相应操作
 * @param   _rx_byte 接收到的字节数据
 * @return  无
 * @note    该函数应在串口接收中断中调用，实时处理接收到的数据
 *          目前实现了VOFA数据接收的解析功能，可根据接收到的数据调整PID参数
 *******************************************************************************/
void vofa_set_data(uint8_t _rx_byte);

#endif
